package Tema1;

import java.util.Scanner;

public class HolaMundo {

	public static void main(String[] args) {
	
			Scanner sc = new Scanner(System.in);
			int num = s.nextInt();
			
			if(num%2==0){
				System.out.println(num + " es par");
			}else{
				System.out.println(num + " es impar");
			}
			
			
			sc.close();
	}
}
